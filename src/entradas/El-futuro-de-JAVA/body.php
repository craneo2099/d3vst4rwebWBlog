          <h2 class="section-heading">Un poco de la historia de JAVA</h2>

          <p class="text-justify"><b>Java</b> ha pasado los 25 años y aún es uno de los lenguajes más populares del mundo, siempre actualizado con las nuevas tendencias. Si bien en el último Tiobe Index ya ha sido superado por Python (y C), aún es un lenguaje que merece ser observado de cara al futuro.</p>

          <p class="text-justify">El lenguaje de programación <i>Java</i> nació en 1995 en los laboratorios de Sun Microsystems (comprado por Oracle), su principal característica es que permite a los desarrolladores escribir el código una vez y ejecutarlo en cualquier dispositivo que tenga instalada la Java Virtual Machine (JVM).</p>

          <p class="text-justify">Actualmente y sólo hablando de dispositivos móviles, <b>Java</b> está instalado en 3 mil millones de dispositivos y grandes empresas como Google o Netflix lo prefieren para sus plataformas.</p>

          <h2 class="section-heading">¿Cuáles son las cartas de Java para seguir vigente los próximos años?</h2>

           <p class="text-justify">En el congreso de programación que quizá sea el más importante de Europa (JBCNConf), analizan los puntos fuertes de <b>Java</b> para el futuro:</p>

          <p class="text-justify">
            <ul>
              <li class="text-justify"><b>Cloud</b>. No es algo nuevo en el desarrollo de Java, sabemos que las aplicaciones nuevas serán concebidas para un entorno en la nube. Gartner nos muestra que más allá de ver un crecimiento de la nube, debemos ver el crecimiento particular de cada tecnología que la conforma. Adicionalmente, Microsoft y Amazon confían plenamente en Java y su integración en la nube pues continuamente crean herramientas y servicios para Java en Azure y Amazon Web Services.</li>
              <li class="text-justify"><b>Kotlin</b>. El lenguaje para JVM que ha experimentado el mayor crecimiento es Kotlin. Tiene sus orígenes en 2016 y desde 2017 es para google el lenguaje oficial a la hora de desarrollar aplicaciones en Android. También hay que destacar el soporte que tiene, pues JetBrains proporciona importantes herramientas que apoyan su éxito.</li>
              <li class="text-justify"><b>Actualizaciones continuas</b>. Desde 2017 Oracle ha mantenido el ritmo de actualizaciones liberando una nueva versión cada 6 meses. Esto permite a los desarrolladores obtener las últimas funciones y mejoras mucho antes, sin tener que esperar los dos o tres años para que se libere una actualización importante. En septiembre del 2021 se libera la versión 17 con soporte a largo plazo, para entonces tendremos que estar atentos a la tasa con la que los programadores se ponen al día con las nuevas versiónes y sus funcionalidades.</li>
              <li class="text-justify"><b>Más proveedores de JVM</b>. Ahora es posible para las empresas generar sus propias versiones de Java Virtual Machine, pues en 2018 Oracle permitió la apertura del mercado para otras compañías, por ejemplo, OpenJDK está siendo utilizada por muchos desarrolladores pues es una versión libre que permite a las empresas pagar por una licencia mucho más barata. Es posible que en el futuro veamos que los desarrolladores se decantas por otras JVM.</li>
              <li class="text-justify"><b>JVM nativo</b>. GraalVM permite reducir la cantidad de pasos para ejecutar una aplicación Java porque es posible compilar código Java de antemano y crear imágenes nativas. No hay duda de que, en este año, los frameworks como Quarkus y Micronaut que soportan GraalVM lucharán por ser los elegidos por la comunidad.</li>
            </ul>  
          </p>

          <p>Si bien la tendencia nos dice que Java no está en su mejor momento, los hechos nos confirman que éste lenguaje aún tiene un largo camino por recorrer.</p>

          

