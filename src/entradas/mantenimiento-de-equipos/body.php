          <h2 class="section-heading">Tipos de mantenimiento</h2>

          <p class="text-justify">Existen 2 tipos de mantenimientos de equipos, a continuación los explicamos de forma concisa:</p>

          <h3 id="" class="mt-3 pt-2">Mantenimiento preventivo</h3>

          <p class="text-justify">Es el más común, su objetivo es evitar posibles fallos y mejorar el desempeño del equipo, su función alargar la vida útil del equipo y evitar la falla de sus componentes, asegurando la continuidad de los procesos que está desempeñando y mantener la operatividad de la empresa o negocio.</p>

          <h3 id="" class="mt-3 pt-2">Mantenimiento correctivo</h3>

          <p class="text-justify">Es aquel que está orientado al diagnóstico y reparación del equipo cuando se presenta un problema técnico, este tipo de mantenimiento se lleva a cabo cuando un equipo presenta interrupción o fallas en funcionamiento y se pretende conseguir que este vuelva a estar operativo y en óptimas condiciones. Para lograrlo, se realizarán intervenciones de reparación o sustitución en función de las necesidades propias de cada dispositivo</p>

          <hr>

          <p class="text-justify">Se ha hablado de otros tipos de mantenimiento como el <b>predictivo</b>, donde por medio monitorización se pueden determinar “ventanas” para suspender parcial o totalmente el funcionamiento de los equipos con el objetivo de brindarles mantenimiento, otro tipo es el <b>evolutivo</b> donde se pretende hacer actualizaciones de hardware o software de los equipos para que puedan seguir manteniendo su rendimiento y funciones, sin embargo estos dos pueden ser englobados en la categoría de <b>mantenimiento preventivo</b>.</p>

