          <h2 class="section-heading">¿Qué es un punto de venta?</h2>

          <p class="text-justify">A un <b>punto de venta</b> también se le conoce por sus siglas en ingles POS (point of sale) es un software informático creado para llevar el control de ventas e inventarios de un negocio, que se apoya de herramientas de hardware o periféricos que le permiten llevar sus tareas a cabo de manera rápida y eficaz, estas herramientas pueden ser: lectores de códigos de barras, impresoras, cajas automáticas, equipos de cómputo, etc.</p>

          <p class="text-justify">A continuación hablaremos como los <b>puntos de venta</b> pueden ayudar a tu negocio y también sobre los diferentes tipos que existen pero haremos especial hincapié en los puntos que debes de tener en cuenta para escoger el mejor para tu negocio .</p>

          <p class="text-justify">¿Suena interesante, verdad? ¡Acompañanos!</p>

