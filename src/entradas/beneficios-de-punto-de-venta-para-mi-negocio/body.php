          <h2 class="section-heading">¿Cómo un punto de venta puede ayudar a tu negocio?</h2>

          <p class="text-justify">Un sistema de punto de venta es un conjunto de interacciones entre software y hardware que ayuda al personal de un negocio a automatizar las tareas que intervienen en un proceso de ventas, por ejemplo: órdenes de venta, devoluciones, obtención de reportes, etc. El punto de venta puede contar con módulos o herramientas adicionales tales como el control de inventario y hasta módulos de contabilidad.</p>

          <p class="text-justify">Al contar con un <b>punto de venta</b> puedes realizar tareas más rápidamente y también automatizarlas, al ser un negocio más eficiente verás un aumento en las ventas y en el crecimiento de tu negocio y lo principal ofrecerás una mejor experiencia a tus clientes dando un servicio más eficiente y ágil.</p>

          <p class="text-justify">Un <b>punto de venta</b> es útil no solo para las tiendas departamentales y empresas grandes sino también para los pequeños y medianos negocios ayudándoles a realizar sus tareas más fácilmente y también a tener su información organizada y a la mano.</p>

          <h2 class="section-heading">¿Qué tipos de punto de venta existen?</h2>

          <p class="text-justify">En la actualidad, existen dos principales tipos de puntos de venta:</p>

           <p class="text-justify">
            <ul>
              <li class="text-justify"><b>El punto de venta Local</b> (tradicional), donde la información se almacena en un equipo de cómputo  y se opera sin la necesidad de conexión a internet.</li>
              <li class="text-justify"><b>El punto de venta en la nube</b>, almacena la información en un servidor de internet y se ejecuta a través de un navegador web brindando portabilidad no solo en computadoras sino en dispositivos móviles como celulares y tablets.</li>
            </ul>  
          </p>

          <h3 id="" class="mt-3 pt-2">Punto de venta Local (offline)</h3>

          <p class="text-justify">Un sistema <b>punto de venta tradicional</b> funciona a través de la instalación directa de un software y equipo de hardware (periféricos) en equipos conectados a una red de computadoras interna o si el negocio es pequeño en una sola computadora (standalone). Cuando es en más de una computadora, generalmente requieren de un servidor local para almacenar la información y se actualizan de forma manual con el apoyo de los proveedores.</p>

          <p class="text-justify"><b>Ventajas</b>
            <ul>
              <li class="text-justify">Pueden ser más robustas.</li>
              <li class="text-justify">Tiempo de respuesta más rápido.</li>
              <li class="text-justify">Se puede hacer cualquier cosa que permita el Software (cuestión gráfica, control total de las entradas del usuario al momento de capturar).</li>
              <li class="text-justify">Facilita el uso de atajos por medio de hotkeys.</li>
            </ul>  
          </p>

          <p class="text-justify"><b>Desventajas</b>
            <ul>
              <li class="text-justify">Requiere instalación en cada cliente (equipo de computo).</li>
              <li class="text-justify">Generalmente se hacen para un Sistema Operativo especifico.</li>
              <li class="text-justify">Se requiere actualizar en cada cliente.</li>
            </ul>  
          </p>

          <h3 id="" class="mt-3 pt-2">Punto de venta en la nube</h3>

          <p class="text-justify">No necesita instalación ya que se puede acceder al sistema y sus funciones por medio de un navegador web. El <i>punto de venta</i> en la nube se encarga de almacenar todos los datos del negocio en un servidor alojado en línea y facilita que los usuarios puedan <b>acceder a su información desde cualquier lugar con conexión a internet.</b></p>

          <p class="text-justify">Puede ser consultado desde cualquier dispositivo con conectividad. Los puntos de venta online pueden funcionar de dos formas:</p>
          
          <h4 class="mt-3 pt-2">1. Como punto de venta web (online)</h4>

          <p class="text-justify">El punto de venta web se ejecuta en un navegador de internet y funciona totalmente en línea. Esta alternativa ha ganado atracción entre los pequeños y medianos negocios debido a la facilidad de utilizarse desde cualquier dispositivo con acceso a internet, pero presenta el riesgo de no poder operar ante un problema de conexión de internet.</p>

          <h4 class="mt-3 pt-2">2. Como punto de venta híbrido (online/offline)</h4>

          <p class="text-justify">En este punto de venta híbrido <b>No es necesario que todo el tiempo tenga conexión</b>, permitiendo que los usuarios puedan generar ventas y agregar pagos sin conexión a internet.</p>

          <p class="text-justify">Una vez que el servicio se restablece, el punto de venta híbrido se encarga de sincronizar la información en la nube automáticamente, sin temor a tener que suspender las ventas por la falta de internet.</p>

          <p class="text-justify">ya que indagamos en esta modalidad de <i>Punto de venta en la nube</i>, ahora analicemos lo bueno y lo malo:</p>

          <p class="text-justify"><b>Ventajas</b>
            <ul>
              <li class="text-justify">Se puede acceder y usar desde cualquier lugar.</li>
              <li class="text-justify">No requiere hacer actualizaciones en los clientes.</li>
              <li class="text-justify">No hay problemas de incompatibilidad entre versiones, porque todos trabajan con la misma.</li>
              <li class="text-justify">Se centralizan los respaldos de información.</li>
              <li class="text-justify">No necesita instalar nada en el cliente, agregar una nueva terminal solo requiere poner una computadora nueva.</li>
              <li class="text-justify">No se obliga a usar un determinado sistema operativo.</li>
            </ul>  
          </p>

          <p class="text-justify"><b>Desventajas</b>
            <ul>
              <li class="text-justify">Requiere conexión a la red.</li>
              <li class="text-justify">Toma mas tiempo de desarrollo haciéndola compatible con los distinto navegadores, los frameworks de desarrollo ayudan a solventar estos problemas.</li>
              <li class="text-justify">Su tiempo de respuesta es relativamente más lento, sin embargo esto ha mejorado haciéndolas casi tan rápidas como las de escritorio.</li>
            </ul>  
          </p>

